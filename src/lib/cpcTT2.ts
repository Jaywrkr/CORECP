// Compara un código CPC detectado en el pliego contra la Tabla 2 (TT2).
// Normaliza dejando solo dígitos para tolerar espacios/guiones en el texto
// del pliego (ej. "CPC 9 – 4523 00044" vs "452300044").
export function buscarCoincidenciaTT2(codigoDetectado: string): CpcTT2Entry | undefined {
  const normalizado = codigoDetectado.replace(/\D/g, "");
  if (!normalizado) return undefined;
  return CPC_TT2.find((entry) => entry.codigo === normalizado);
}
